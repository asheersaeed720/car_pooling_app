                              Expanded(
                                child: ElevatedButton(
                                  child: Text('Reschedule'),
                                  onPressed: () => {},
                                ),
                              )
                            ],
                          )
                        ],
                      ),
                    ),
                  );
                },
              ),
            )
          ],
        ),
      ),
    );
  }
